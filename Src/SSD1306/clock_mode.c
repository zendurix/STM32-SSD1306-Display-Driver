#include "clock_mode.h"

void ClockHHMM_init(ClockHHMM *clock, uint8_t hour, uint8_t minute,
		uint32_t milliseconds) {
	if (!clock) {
		return;
	}

	clock->hour = hour;
	clock->minute = minute;

	clock->last_minute_time_stamp = milliseconds;
	clock->is_dirty = true;
}

void ClockHHMM_update(ClockHHMM *clock, uint32_t milliseconds) {
	if (!clock) {
		return;
	}

	bool minute_passed = (milliseconds - clock->last_minute_time_stamp)
			>= 60000;

	if (minute_passed) {
		clock->is_dirty = true;
		clock->minute++;
		clock->last_minute_time_stamp = milliseconds;

		if (clock->minute >= 60) {
			clock->minute = 0;
			clock->hour++;

			if (clock->hour >= 24) {
				clock->hour = 0;
			}
		}
	}
}

void ClockHHMM_update_display(ClockHHMM *clock, Display *display) {
	if (!clock || !display || !clock->is_dirty) {
		return;
	}

	Display_clear_buffer(display);

	int8_t h0 = clock->hour / 10;
	int8_t h1 = clock->hour % 10;

	int8_t m0 = clock->minute / 10;
	int8_t m1 = clock->minute % 10;

	add_to_buffer_digit(display, h0, DigitPos_HH_0);
	add_to_buffer_digit(display, h1, DigitPos_HH_1);

	add_to_buffer_colon(display);

	add_to_buffer_digit(display, m0, DigitPos_MM_0);
	add_to_buffer_digit(display, m1, DigitPos_MM_1);

	Display_draw_buffer(display);

	clock->is_dirty = false;
}

void add_to_buffer_colon(Display *display) {
	int8_t pixels_y[8] = { 8, 9, 10, 11, 20, 21, 22, 23 };

	for (int y = 0; y < 8; y++) {
		Display_set_pixel(display, 63, pixels_y[y], true);
		Display_set_pixel(display, 64, pixels_y[y], true);
		Display_set_pixel(display, 65, pixels_y[y], true);
		Display_set_pixel(display, 66, pixels_y[y], true);
	}
}

